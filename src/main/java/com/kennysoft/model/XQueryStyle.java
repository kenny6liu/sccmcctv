package com.kennysoft.model;

public enum XQueryStyle {
    Between, //介于
    EqualTo,//等于
    GreaterThan,//大于
    GreaterThanOrEqualTo,//大于或等于 ===不小于
    In,//包含
    IsNotNull,//不为空
    IsNull,//为空
    LessThan,//小于
    LessThanOrEqualTo,//少于或等于 ===不大于
    Like,// 像
    NotBetween,//不介于
    NotEqualTo, //不等于
    NotLike,//不像
    NotIn//不包含

}